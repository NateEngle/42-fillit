#include "header.h"

int		place_tet(char **piece, char **map, int x, int y)
{
	int i;
	int j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			if (piece[j][i] == '#' && map[y + j][x + i] != '.')
				return (0);
			j++;
		}
		i++;
	}
	return (1);
}